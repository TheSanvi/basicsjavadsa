package ifelse;

public class Comparisons {
    public static void main(String[] args) {
        int a = 3, b = 2;
        System.out.println(a < b);
        System.out.println(a <= b);
        System.out.println(a > b);
        System.out.println(a >= b);
        System.out.println(a == b);
        System.out.println(a != b);
    }
}
